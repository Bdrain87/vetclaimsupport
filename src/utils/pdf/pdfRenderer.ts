/**
 * VCS Brand PDF Renderer
 *
 * Shared rendering utilities for all VCS PDF exports. Uses the brand
 * gold/black/cream palette and provides consistent layout primitives.
 *
 * All doctor-facing documents include a "PATIENT-PREPARED" watermark
 * and 38 CFR Part 14 disclaimer on every page.
 */
import type jsPDFType from 'jspdf';
import { PDF_BRAND, type RGBColor } from './pdfColors';
import { DOCTOR_SUMMARY_DISCLAIMER } from '@/utils/bannedPhrases';
import { guardExport, type ExportGuardResult } from '@/utils/exportGuard';

// ── Errors ──────────────────────────────────────────────────────

export class ExportBlockedError extends Error {
  guardResult: ExportGuardResult;
  constructor(result: ExportGuardResult) {
    super(result.message);
    this.name = 'ExportBlockedError';
    this.guardResult = result;
  }
}

export function assertExportClean(text: string): void {
  const result = guardExport(text);
  if (!result.allowed) throw new ExportBlockedError(result);
}

// ── Loader ──────────────────────────────────────────────────────

export const loadJsPDF = async () => {
  const { default: jsPDF } = await import('jspdf');
  return jsPDF;
};

// ── Constants ───────────────────────────────────────────────────

const MARGIN_LEFT = 20;
const MARGIN_RIGHT = 20;
const FOOTER_HEIGHT = 50;

const DISCLAIMER_TEXT = [
  'DISCLAIMER: Generated by Vet Claim Support. Not VA-accredited (38 C.F.R. Part 14).',
  'This document is patient-organized clinical information for educational and personal preparation only.',
  'It does not constitute legal or medical advice, does not file claims, and provides no medical opinions.',
  'Not affiliated with the U.S. Department of Veterans Affairs.',
];

// ── Document Creation ───────────────────────────────────────────

export interface CreatePDFOptions {
  /** Whether to add PATIENT-PREPARED watermark on each page */
  watermark?: boolean;
  /** Whether this is doctor-facing (adds doctor disclaimer) */
  doctorFacing?: boolean;
}

/**
 * Creates a new jsPDF document with VCS brand defaults.
 * Registers afterPage hook for automatic footer + watermark.
 */
export async function createPDFDoc(options: CreatePDFOptions = {}) {
  const JsPDF = await loadJsPDF();
  const doc = new JsPDF({ unit: 'pt', format: 'letter', compress: true });

  // Track page numbers for footer rendering during save
  const state = {
    totalPages: 0,
    watermark: options.watermark ?? false,
    doctorFacing: options.doctorFacing ?? false,
  };

  return { doc, state };
}

/**
 * Finalize the document: add footer/watermark to every page and return blob.
 */
export function finalizePDF(
  doc: jsPDFType,
  state: { totalPages?: number; watermark: boolean; doctorFacing: boolean },
): Blob {
  const totalPages = doc.getNumberOfPages();
  for (let i = 1; i <= totalPages; i++) {
    doc.setPage(i);
    addBrandFooter(doc, i, totalPages, state.doctorFacing);
    if (state.watermark) {
      addWatermark(doc, 'PATIENT-PREPARED');
    }
  }
  return doc.output('blob');
}

// ── Header ──────────────────────────────────────────────────────

export interface BrandHeaderOptions {
  title: string;
  subtitle?: string;
  date?: Date;
}

/**
 * Adds a branded header with gold title, gold rule, subtitle, and date.
 * Returns the Y position after the header.
 */
export function addBrandHeader(doc: jsPDFType, options: BrandHeaderOptions, yPos: number = 40): number {
  const pageWidth = doc.internal.pageSize.getWidth();
  const date = options.date || new Date();

  // Title in gold
  doc.setFontSize(20);
  doc.setTextColor(...PDF_BRAND.goldDark);
  doc.setFont('helvetica', 'bold');
  doc.text(options.title, MARGIN_LEFT, yPos);

  // Gold rule under title
  yPos += 6;
  doc.setDrawColor(...PDF_BRAND.goldMedium);
  doc.setLineWidth(1.5);
  doc.line(MARGIN_LEFT, yPos, pageWidth - MARGIN_RIGHT, yPos);

  yPos += 16;

  // Subtitle
  if (options.subtitle) {
    doc.setFontSize(11);
    doc.setTextColor(...PDF_BRAND.textSecondary);
    doc.setFont('helvetica', 'normal');
    doc.text(options.subtitle, MARGIN_LEFT, yPos);
    yPos += 14;
  }

  // Generated date
  doc.setFontSize(8);
  doc.setTextColor(...PDF_BRAND.textMuted);
  doc.text(`Generated: ${date.toLocaleDateString()} at ${date.toLocaleTimeString()}`, MARGIN_LEFT, yPos);

  return yPos + 16;
}

// ── Footer ──────────────────────────────────────────────────────

function addBrandFooter(doc: jsPDFType, pageNum: number, totalPages: number, doctorFacing: boolean): void {
  const pageHeight = doc.internal.pageSize.getHeight();
  const pageWidth = doc.internal.pageSize.getWidth();
  const footerTop = pageHeight - FOOTER_HEIGHT;

  // Dark footer bar
  doc.setFillColor(...PDF_BRAND.black);
  doc.rect(0, footerTop, pageWidth, FOOTER_HEIGHT, 'F');

  // Disclaimer text in white
  doc.setFontSize(6.5);
  doc.setTextColor(...PDF_BRAND.white);
  doc.setFont('helvetica', 'normal');

  let textY = footerTop + 10;
  for (const line of DISCLAIMER_TEXT) {
    doc.text(line, pageWidth / 2, textY, { align: 'center' });
    textY += 8;
  }

  // Doctor-facing extra disclaimer
  if (doctorFacing) {
    doc.setFontSize(6);
    doc.setTextColor(200, 200, 200);
    doc.text(DOCTOR_SUMMARY_DISCLAIMER, pageWidth / 2, textY + 2, {
      align: 'center',
      maxWidth: pageWidth - 60,
    });
  }

  // Page number
  doc.setFontSize(7);
  doc.setTextColor(180, 180, 180);
  doc.text(`Page ${pageNum} of ${totalPages}`, pageWidth - MARGIN_RIGHT, footerTop + 10, { align: 'right' });
}


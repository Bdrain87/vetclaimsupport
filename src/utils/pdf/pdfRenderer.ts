// ── Watermark ───────────────────────────────────────────────────

function addWatermark(doc: jsPDFType, text: string): void {
  const pageWidth = doc.internal.pageSize.getWidth();
  const pageHeight = doc.internal.pageSize.getHeight();

  doc.saveGraphicsState();
  doc.setFontSize(48);
  doc.setTextColor(...PDF_BRAND.watermark);
  doc.setFont('helvetica', 'bold');

  // Rotate and center the watermark
  const centerX = pageWidth / 2;
  const centerY = pageHeight / 2;

  // jsPDF text rotation via transform
  doc.text(text, centerX, centerY, {
    align: 'center',
    angle: 45,
  });
  doc.restoreGraphicsState();
}

// ── Page Break Helper ───────────────────────────────────────────

/**
 * Checks if there's enough room for the next content block.
 * If not, adds a page and returns the starting Y.
 */
export function checkPageBreak(doc: jsPDFType, yPos: number, neededHeight: number = 40): number {
  const pageHeight = doc.internal.pageSize.getHeight();
  if (yPos + neededHeight > pageHeight - FOOTER_HEIGHT - 10) {
    doc.addPage();
    return 40; // top margin on new page
  }
  return yPos;
}

// ── Safe Date Helper ────────────────────────────────────────────

export function safeDate(dateStr: string | undefined | null): string {
  if (!dateStr) return 'N/A';
  const d = new Date(dateStr);
  return isNaN(d.getTime()) ? 'N/A' : d.toLocaleDateString();
}

// ── Spacing ─────────────────────────────────────────────────────

export function addSpacer(doc: jsPDFType, yPos: number, height: number = 8): number {
  return checkPageBreak(doc, yPos + height);
}

// ── Table ───────────────────────────────────────────────────────

export interface TableColumn {
  header: string;
  width: number; // points
  align?: 'left' | 'center' | 'right';
}

export interface TableRow {
  cells: string[];
  bold?: boolean;
}

/**
 * Draws a table with gold header row and clean body rows.
 * Returns yPos after the table.
 */
export function addTable(
  doc: jsPDFType,
  columns: TableColumn[],
  rows: TableRow[],
  yPos: number,
): number {
  const pageWidth = doc.internal.pageSize.getWidth();
  const tableWidth = pageWidth - MARGIN_LEFT - MARGIN_RIGHT;
  const rowHeight = 18;
  const headerHeight = 20;

  yPos = checkPageBreak(doc, yPos, headerHeight + rowHeight * Math.min(rows.length, 3) + 10);

  // Header row
  doc.setFillColor(...PDF_BRAND.goldDark);
  doc.rect(MARGIN_LEFT, yPos, tableWidth, headerHeight, 'F');

  doc.setFontSize(8);
  doc.setTextColor(...PDF_BRAND.white);
  doc.setFont('helvetica', 'bold');

  let x = MARGIN_LEFT + 6;
  for (const col of columns) {
    const textX = col.align === 'right' ? x + col.width - 6 : col.align === 'center' ? x + col.width / 2 : x;
    doc.text(col.header, textX, yPos + 13, { align: col.align || 'left' });
    x += col.width;
  }

  yPos += headerHeight;

  // Body rows
  doc.setFont('helvetica', 'normal');
  doc.setFontSize(8);

  for (let r = 0; r < rows.length; r++) {
    yPos = checkPageBreak(doc, yPos, rowHeight + 4);

    // Alternate row bg
    if (r % 2 === 0) {
      doc.setFillColor(...PDF_BRAND.cream);
      doc.rect(MARGIN_LEFT, yPos, tableWidth, rowHeight, 'F');
    }

    doc.setTextColor(...PDF_BRAND.textPrimary);
    if (rows[r].bold) doc.setFont('helvetica', 'bold');
    else doc.setFont('helvetica', 'normal');

    x = MARGIN_LEFT + 6;
    for (let c = 0; c < columns.length; c++) {
      const cellText = rows[r].cells[c] || '';
      const col = columns[c];
      // Truncate to fit column
      const maxWidth = col.width - 12;
      const lines = doc.splitTextToSize(cellText, maxWidth);
      const displayText = lines.length > 1 ? lines[0].slice(0, -3) + '...' : lines[0] || '';
      const textX = col.align === 'right' ? x + col.width - 6 : col.align === 'center' ? x + col.width / 2 : x;
      doc.text(displayText, textX, yPos + 12, { align: col.align || 'left' });
      x += col.width;
    }

    yPos += rowHeight;
  }

  // Bottom border
  doc.setDrawColor(...PDF_BRAND.border);
  doc.setLineWidth(0.5);
  doc.line(MARGIN_LEFT, yPos, MARGIN_LEFT + tableWidth, yPos);

  return yPos + 8;
}

// ── Highlight Box ───────────────────────────────────────────────

/**
 * Draws a cream-colored highlight box with gold left border.
 * Supports multi-line text with auto-wrapping.
 */
export function addHighlightBox(
  doc: jsPDFType,
  text: string,
  yPos: number,
  options?: { label?: string; type?: 'info' | 'success' | 'warning' | 'danger' },
): number {
  const pageWidth = doc.internal.pageSize.getWidth();
  const boxWidth = pageWidth - MARGIN_LEFT - MARGIN_RIGHT;
  const maxTextWidth = boxWidth - 20;

  const lines = doc.splitTextToSize(text, maxTextWidth);
  const labelHeight = options?.label ? 14 : 0;
  const boxHeight = lines.length * 10 + 16 + labelHeight;

  yPos = checkPageBreak(doc, yPos, boxHeight + 4);

  // Background
  doc.setFillColor(...PDF_BRAND.cream);
  doc.roundedRect(MARGIN_LEFT, yPos, boxWidth, boxHeight, 3, 3, 'F');

  // Left accent bar
  const accentColor: RGBColor =
    options?.type === 'success' ? PDF_BRAND.success :
    options?.type === 'warning' ? PDF_BRAND.warning :
    options?.type === 'danger' ? PDF_BRAND.danger :
    PDF_BRAND.goldMedium;
  doc.setFillColor(...accentColor);
  doc.rect(MARGIN_LEFT, yPos, 4, boxHeight, 'F');

  let textY = yPos + 12;

  // Optional label
  if (options?.label) {
    doc.setFontSize(8);
    doc.setTextColor(...accentColor);
    doc.setFont('helvetica', 'bold');
    doc.text(options.label.toUpperCase(), MARGIN_LEFT + 14, textY);
    textY += 12;
  }

  // Body text
  doc.setFontSize(8.5);
  doc.setTextColor(...PDF_BRAND.textPrimary);
  doc.setFont('helvetica', 'normal');
  for (const line of lines) {
    doc.text(line, MARGIN_LEFT + 14, textY);
    textY += 10;
  }

  return yPos + boxHeight + 6;
}

// ── Stat Box ────────────────────────────────────────────────────

export interface StatItem {
  label: string;
  value: string;
}

/**
 * Draws a row of stat boxes (value + label pairs) in cream cards.
 */
export function addStatBox(doc: jsPDFType, stats: StatItem[], yPos: number): number {
  const pageWidth = doc.internal.pageSize.getWidth();
  const totalWidth = pageWidth - MARGIN_LEFT - MARGIN_RIGHT;
  const boxWidth = (totalWidth - (stats.length - 1) * 8) / stats.length;
  const boxHeight = 44;

  yPos = checkPageBreak(doc, yPos, boxHeight + 8);

  for (let i = 0; i < stats.length; i++) {
    const x = MARGIN_LEFT + i * (boxWidth + 8);

    // Card bg
    doc.setFillColor(...PDF_BRAND.cream);
    doc.roundedRect(x, yPos, boxWidth, boxHeight, 3, 3, 'F');

    // Gold top accent
    doc.setFillColor(...PDF_BRAND.goldMedium);
    doc.rect(x, yPos, boxWidth, 3, 'F');

    // Value
    doc.setFontSize(16);
    doc.setTextColor(...PDF_BRAND.goldDark);
    doc.setFont('helvetica', 'bold');
    doc.text(stats[i].value, x + boxWidth / 2, yPos + 22, { align: 'center' });

    // Label
    doc.setFontSize(7);
    doc.setTextColor(...PDF_BRAND.textSecondary);
    doc.setFont('helvetica', 'normal');
    doc.text(stats[i].label, x + boxWidth / 2, yPos + 36, { align: 'center' });
  }

  return yPos + boxHeight + 10;
}

// ── Key-Value Pair ──────────────────────────────────────────────

/**
 * Renders a label: value pair on a single line.
 */
export function addKeyValue(doc: jsPDFType, label: string, value: string, yPos: number): number {
  yPos = checkPageBreak(doc, yPos, 14);
  doc.setFontSize(8.5);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(...PDF_BRAND.textSecondary);
  doc.text(`${label}:`, MARGIN_LEFT, yPos);

  const labelWidth = doc.getTextWidth(`${label}: `);
  doc.setFont('helvetica', 'normal');
  doc.setTextColor(...PDF_BRAND.textPrimary);
  doc.text(value || 'Not provided', MARGIN_LEFT + labelWidth + 2, yPos);

  return yPos + 13;
}

// ── Bullet List ─────────────────────────────────────────────────

/**
 * Renders a bulleted list of items.
 */
export function addBulletList(doc: jsPDFType, items: string[], yPos: number): number {
  const pageWidth = doc.internal.pageSize.getWidth();
  doc.setFontSize(8.5);
  doc.setFont('helvetica', 'normal');
  doc.setTextColor(...PDF_BRAND.textPrimary);

  for (const item of items) {
    yPos = checkPageBreak(doc, yPos, 12);
    doc.text('\u2022', MARGIN_LEFT + 4, yPos);
    const lines = doc.splitTextToSize(item, pageWidth - MARGIN_LEFT - MARGIN_RIGHT - 20);
    for (const line of lines) {
      doc.text(line, MARGIN_LEFT + 14, yPos);
      yPos += 11;
    }
  }

  return yPos + 2;
}


/**
 * Look up C&P exam details by condition ID.
 */
export function getCPExamDetails(conditionId: string): CPExamDetail | undefined {
  return cpExamDetails.find(
    (d) => d.conditionId === conditionId.toLowerCase()
  );
}

/**
 * Search C&P exam details by query string.
 * Matches against conditionId, conditionName, diagnosticCode, and examType.
 */
export function searchCPExamDetails(query: string): CPExamDetail[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];
  return cpExamDetails.filter(
    (d) =>
      d.conditionId.toLowerCase().includes(q) ||
      d.conditionName.toLowerCase().includes(q) ||
      d.diagnosticCode.toLowerCase().includes(q) ||
      d.examType.toLowerCase().includes(q)
  );
}